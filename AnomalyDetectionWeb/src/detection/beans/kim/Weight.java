package detection.beans.kim;

public class Weight
{
	private int id;
	private String stock_name;
	private String peer_name;
	private float eduDistance;
	private float proxi;
	private float weight;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getStock_name()
	{
		return stock_name;
	}
	public void setStock_name(String stock_name)
	{
		this.stock_name = stock_name;
	}
	public String getPeer_name()
	{
		return peer_name;
	}
	public void setPeer_name(String peer_name)
	{
		this.peer_name = peer_name;
	}
	public float getEduDistance()
	{
		return eduDistance;
	}
	public void setEduDistance(float eduDistance)
	{
		this.eduDistance = eduDistance;
	}
	public float getProxi()
	{
		return proxi;
	}
	public void setProxi(float proxi)
	{
		this.proxi = proxi;
	}
	public float getWeight()
	{
		return weight;
	}
	public void setWeight(float weight)
	{
		this.weight = weight;
	}
	
	
}
